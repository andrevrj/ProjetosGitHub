    private void InserirBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_InserirBtnActionPerformed
        // TODO add your handling code here:
        
        Diretor d1 = new Diretor();
        d1.setNome(diretorNome.getText());
        d1.setCpf(diretorCPF.getText());
        d1.setIdade(Integer.parseInt(diretorIdade.getText()));
        
        Filme f1 = new Filme();
        f1.setNome(filmeNome.getText());
        f1.setGenero(filmeGenero.getText());
        f1.setDiretor_cpf(diretorCPF.getText());
     
        Pessoa p1 = new Pessoa();
        p1.setNome(pessoaNome.getText());
        p1.setRg(pessoaRG.getText());
        p1.setIdade(Integer.parseInt(pessoaRG.getText()));
        
        Ingresso i1 = new Ingresso();
        i1.setValor(Float.parseFloat(ingressoValor.getText()));
        i1.setPoltrona(Integer.parseInt(ingressoPoltrona.getText()));
        i1.setPessoa_rg(pessoaRG.getText());
        
        // Instanciando objeto DAO
        DiretorDAO dd1 = new DiretorDAO();
        FilmeDAO fd1 = new FilmeDAO();
        IngressoDAO id1 = new IngressoDAO();
        PessoaDAO pd1 = new PessoaDAO();
        
        
        // Inserindo no banco de dados
        if(dd1.inserirDiretor(d1)){
            JOptionPane.showMessageDialog( null,"Diretor inserido com sucesso");
        } else {
            JOptionPane.showMessageDialog( null,"Diretor deu errado!");
        }
        
        if(fd1.inserirFilme(f1)){
            JOptionPane.showMessageDialog( null,"Filme inserido com sucesso");
        } else {
           JOptionPane.showMessageDialog( null,"Filme deu errado!");
        }
        
        if(pd1.inserirPessoa(p1)){
           JOptionPane.showMessageDialog( null,"Pessoa inserido com sucesso");
        } else {
           JOptionPane.showMessageDialog( null,"Pessoa deu errado!");
        }
  
        if(id1.inserirIngresso(i1)){
          JOptionPane.showMessageDialog( null,"Ingresso inserido com sucesso");
        } else {
            JOptionPane.showMessageDialog( null,"Ingresso deu errado!");
        }
        
        
        TelaInicial t2 = new TelaInicial();
        t2.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_InserirBtnActionPerformed

