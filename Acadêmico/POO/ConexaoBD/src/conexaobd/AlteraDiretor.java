    private void alterarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarBtnActionPerformed
        // TODO add your handling code here:
        
         Diretor d1 = new Diretor();
        d1.setCpf(cpfDiretor.getText());
        d1.setNome(nomeDiretor.getText());
        d1.setIdade(Integer.parseInt(idadeDiretor.getText()));
   
        // Instanciando objeto DAO
        DiretorDAO dd1 = new DiretorDAO();
        
        // Atualizando no banco de dados
        if(dd1.atualizarDiretor(d1.getCpf(), d1)){
            JOptionPane.showMessageDialog(null, "Diretor atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Diretor n√£o atualizado!");
        }
        
        // voltando para a tela inicial
        TelaInicial t2 = new TelaInicial();
        t2.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_alterarBtnActionPerformed

