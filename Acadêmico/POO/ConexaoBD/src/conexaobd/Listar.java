    private void listaListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_listaListarActionPerformed
        // TODO add your handling code here:
        
        
        
        DiretorDAO dd1 = new DiretorDAO();
        FilmeDAO fd1 = new FilmeDAO();
        IngressoDAO id1 = new IngressoDAO();
        PessoaDAO pd1 = new PessoaDAO();
        
        dlmDiretor.clear();
        diretores = dd1.buscarDiretoresSemFiltro();
        
        dlmFilme.clear();
        filmes = fd1.buscarFilmesSemFiltro();
        
        dlmIngresso.clear();
        ingressos = id1.buscarIngressosSemFiltro();
        
        dlmPessoa.clear();
        pessoas = pd1.buscarPessoasSemFiltro();
        
        for( int i = 0 ; i < diretores.size() ; i++ ){
            dlmDiretor.addElement("CPF: " + diretores.get(i).getCpf());
            dlmDiretor.addElement("Nome: " + diretores.get(i).getNome());
            dlmDiretor.addElement("Idade: " + diretores.get(i).getIdade());
            dlmDiretor.addElement("-------------------------------------");
        }
        
         for( int i = 0 ; i < filmes.size() ; i++ ){
            dlmFilme.addElement("idFilme: " + filmes.get(i).getIdFilme());
            dlmFilme.addElement("Nome: " + filmes.get(i).getNome());
            dlmFilme.addElement("GÃªnero: " + filmes.get(i).getGenero());
            dlmFilme.addElement("-------------------------------------");
        }
        
         for( int i = 0 ; i < ingressos.size() ; i++ ){
            dlmIngresso.addElement("CPF: " + ingressos.get(i).getCodigo());
            dlmIngresso.addElement("Valor: " + ingressos.get(i).getValor());
            dlmIngresso.addElement("Poltrona: " + ingressos.get(i).getPoltrona());
            dlmIngresso.addElement("-------------------------------------");
        }
        
         for( int i = 0 ; i < pessoas.size() ; i++ ){
            dlmPessoa.addElement("RG: " + pessoas.get(i).getRg());
            dlmPessoa.addElement("Valor: " + pessoas.get(i).getNome());
            dlmPessoa.addElement("Poltrona: " + pessoas.get(i).getIdade());
            dlmPessoa.addElement("-------------------------------------");
        }
         
    }//GEN-LAST:event_listaListarActionPerformed

